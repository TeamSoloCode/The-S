public class MemberLocation {
    private double log;
    private double lat;

    public MemberLocation() {
    }

    public void setLog(double log) {
        this.log = log;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLog() {
        return log;
    }

    public double getLat() {
        return lat;
    }

}
