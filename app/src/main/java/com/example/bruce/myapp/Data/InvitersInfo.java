public class InvitersInfo {
    private String invitersName;
    private String invitersEmail;
    private String teamId;
    private String invitersPhotoURL;
    private boolean checked;
    private String teamsName;

    public InvitersInfo() {
    }

    public String getInvitersName() {
        return invitersName;
    }

    public String getInvitersEmail() {
        return invitersEmail;
    }

    public String getTeamId() {
        return teamId;
    }

    public String getInvitersPhotoURL() {
        return invitersPhotoURL;
    }

    public boolean isChecked() {
        return checked;
    }

    public String getTeamsName() {
        return teamsName;
    }
}
